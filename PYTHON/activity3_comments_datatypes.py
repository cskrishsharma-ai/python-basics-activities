# 1. Integer
age = 20
print("age:",age,type(age))  # <class 'int'>

# 2. Float
height = 5.11
print("Height:",height,"type:",type(height)) # <class 'float'>

# 3. String
name = "krish"
print("name:",name,type(name)) # <class 'str'>

# 4. Boolean
is_student = True
print("Is Student:",is_student,"type:",type(is_student)) # <class 'bool'>

